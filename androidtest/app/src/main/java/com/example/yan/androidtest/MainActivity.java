    private void initPersonData() {
        sortList=new ArrayList<>();

        BmobQuery<Questionsort> query=new BmobQuery<Questionsort>();
        //按照时间降序
        query.order("-createdAt");

        query.findObjects(new FindListener<Questionsort>() {
            @Override
            public void done(List<Questionsort> list, BmobException e) {
                adapter=new RecyclerViewAdapter(list,MainActivity.this);
                recyclerView.setAdapter(adapter);
            }
        });

    }

